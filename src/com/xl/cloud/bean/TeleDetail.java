package com.xl.cloud.bean;
/***
 * 手机号码详细信息
 * @author root
 *
 */
public class TeleDetail {
			private String teleNum;//手机号
			private String belongCity;//城市
			private String caseId;
			private String teleType;//类型
			private String belongProvince;//省份
			private int count=-1;
			private String evid;
			
			public String getEvid() {
				return evid;
			}
			public void setEvid(String evid) {
				this.evid = evid;
			}

			public String getTeleNum() {
				return teleNum;
			}
			public void setTeleNum(String teleNum) {
				this.teleNum = teleNum;
			}
			public String getBelongCity() {
				return belongCity;
			}
			public void setBelongCity(String belongCity) {
				this.belongCity = belongCity;
			}
			public String getCaseId() {
				return caseId;
			}
			public void setCaseId(String caseId) {
				this.caseId = caseId;
			}
			public String getTeleType() {
				return teleType;
			}
			public void setTeleType(String teleType) {
				this.teleType = teleType;
			}
			public String getBelongProvince() {
				return belongProvince;
			}
			public void setBelongProvince(String belongProvince) {
				this.belongProvince = belongProvince;
			}
			public int getCount() {
				return count;
			}
			public void setCount(int count) {
				this.count = count;
			}
			public TeleDetail() {
				super();
				// TODO Auto-generated constructor stub
			}
			public TeleDetail(String teleNum, String belongCity, String caseId, String teleType, String belongProvince,
					int count, String evid) {
				super();
				this.teleNum = teleNum;
				this.belongCity = belongCity;
				this.caseId = caseId;
				this.teleType = teleType;
				this.belongProvince = belongProvince;
				this.count = count;
				this.evid = evid;
			}
	
	
}
