package com.xl.cloud.bean;

public class PriceDetail {
	private int id =-1;
	private String pricenum;//价格号
	private int count=-1;
	private String caseId;
	private String evid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPricenum() {
		return pricenum;
	}
	public void setPricenum(String pricenum) {
		this.pricenum = pricenum;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getCaseId() {
		return caseId;
	}
	public void setCaseId(String caseId) {
		this.caseId = caseId;
	}
	public String getEvid() {
		return evid;
	}
	public void setEvid(String evid) {
		this.evid = evid;
	}
	@Override
	public String toString() {
		return "PriceDetail [id=" + id + ", pricenum=" + pricenum + ", count=" + count + ", caseId=" + caseId
				+ ", evid=" + evid + "]";
	}
	
	

}
