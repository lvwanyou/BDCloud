package com.xl.cloud.bean;

public class HackCount {
	private long gewei;
	private long wanwei;
	private long yiwei;
	public HackCount(){
		gewei=0;
		wanwei=0;
		yiwei=0;
	}
	
	public long getGewei(){
		return this.gewei;
	}
	public void setGewei(long gewei){
		this.gewei=gewei;	
	}
	
	public long getWanwei(){
		return this.wanwei;
	}
	public void setWanwei(long wanwei){
		this.wanwei=wanwei;	
	}
	
	public long getYiwei(){
		return this.yiwei;
	}
	public void setYiwei(long yiwei){
		this.yiwei=yiwei;	
	}
}
