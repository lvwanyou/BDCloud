package com.xl.cloud.bean;

public class IndexQueue {

	private int id = -1;
	private int caseID = -1;//所属案件ID
	private String userName;//登陆用户
	private String caseName;//案件名称
	private String dataName;//数据名称
	private String dataType;//数据类型
	private String dataSize;//数据大小
	private int runningState=-1;//运行状态
	private String startTime;//开始时间
	private String endTime;//停止时间
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCaseID() {
		return caseID;
	}
	public void setCaseID(int caseID) {
		this.caseID = caseID;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCaseName() {
		return caseName;
	}
	public void setCaseName(String caseName) {
		this.caseName = caseName;
	}
	public String getDataName() {
		return dataName;
	}
	public void setDataName(String dataName) {
		this.dataName = dataName;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public String getDataSize() {
		return dataSize;
	}
	public void setDataSize(String dataSize) {
		this.dataSize = dataSize;
	}
	public int getRunningState() {
		return runningState;
	}
	public void setRunningState(int runningState) {
		this.runningState = runningState;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	
}
