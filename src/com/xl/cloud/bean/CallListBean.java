package com.xl.cloud.bean;

/**
 * 话单分析的相关字段
 *
 * @author YangChen
 * @create 2017-11-15 16:29
 */

public class CallListBean {
    /**用户姓名*/
    private String name;
    /**用户电话号码*/
    private String localNum;
    /**通话开始时间*/
    private String startTime;
    /**通信地点*/
    private String position;
    /**通信方式*/
    private String method;
    /**对方号码*/
    private String dialNumber;
    /**通信时长*/
    private String callDuration;
    private String file_download_url;
    private String caseID;
    /**通话日期*/
    private String day;
    /**通话时间*/
    private String time;
    /**通话类型*/
    private String callType;
    /**通话所在地*/
    private String callHome;

    public CallListBean(){

    }
    public CallListBean(String name, String localNum,String startTime, String position, String method,
                        String dialNumber,String callDuration,String file_download_url,String caseID,String day,String time,String callType, String callHome) {
        this.name = name;
        this.localNum = localNum;
        this.startTime = startTime;
        this.position = position;
        this.method = method;
        this.dialNumber = dialNumber;
        this.callDuration = callDuration;
        this.file_download_url = file_download_url;
        this.caseID = caseID;
        this.day = day;
        this.time = time;
        this.callType = callType;
        this.callHome = callHome;
    }

    public String getCaseID() {
        return caseID;
    }
    public void setCaseID(String caseID) {
        this.caseID = caseID;
    }
    public String getFile_download_url() {
        return file_download_url;
    }
    public void setFile_download_url(String file_download_url) {
        this.file_download_url = file_download_url;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getLocalNum() {
        return localNum;
    }
    public void setLocalNum(String localNum) {
        this.localNum = localNum;
    }
    public String getStartTime() {
        return startTime;
    }
    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }
    public String getPosition() {
        return position;
    }
    public void setPosition(String position) {
        this.position = position;
    }
    public String getMethod() {
        return method;
    }
    public void setMethod(String method) {
        this.method = method;
    }
    public String getDialNumber() {
        return dialNumber;
    }
    public void setDialNumber(String dialNumber) {
        this.dialNumber = dialNumber;
    }
    public String getCallDuration() {
        return callDuration;
    }
    public void setCallDuration(String callDuration) {
        this.callDuration = callDuration;
    }

    public String getDay() {
        return day;
    }
    public void setDay(String day) {
        this.day = day;
    }
    public String getTime() {
        return time;
    }
    public void setTime(String time) {
        this.time = time;
    }
    public String getCallType() {
        return callType;
    }

    public void setCallType(String callType) {
        this.callType = callType;
    }

    public String getCallHome() {
        return callHome;
    }

    public void setCallHome(String callHome) {
        this.callHome = callHome;
    }

    @Override
    public String toString() {
        return "CallListBean [name=" + name + ", localNum=" + localNum
                + ", startTime=" + startTime + ", position=" + position
                + ", method=" + method + ", dialNumber=" + dialNumber
                + ", callDuration=" + callDuration + ", file_download_url="
                + file_download_url + ", caseID=" + caseID + ", day=" + day
                + ", time=" + time + ", callType=" + callType + ", callHome=" + callHome+"]";
    }

}
