package com.xl.cloud.phoneBean;

//微信通讯录下的页面对象
public class WeChatContacts {
	//微信ID
	private String wxId;
	
	//好友账号
	private String friendAcc;
	
	//好友昵称
	private String friendNick;
	
	//性别
	private String sex;
	
	//个性签名
	private String signature;
	
	//备注
	private String remarks;
	
	//所在地
	private String location;
	
	//头像
	private String avatar;
	
	//qq账号
	private String qq;
	
	//qq昵称
	private String qqNick;
	
	//邮箱地址
	private String mail;
	
	//移动电话
	private String telephone;
	
	//描述
	private String description;
	
	//标签
	private String tab;
	
	//md5加密
	private String md5;
	
	public String getWxId() {
		return wxId;
	}

	public void setWxId(String wxId) {
		this.wxId = wxId;
	}

	public String getFriendAcc() {
		return friendAcc;
	}

	public void setFriendAcc(String friendAcc) {
		this.friendAcc = friendAcc;
	}

	public String getFriendNick() {
		return friendNick;
	}

	public void setFriendNick(String friendNick) {
		this.friendNick = friendNick;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getSignature() {
		return signature;
	}

	public void setSignature(String signature) {
		this.signature = signature;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getQq() {
		return qq;
	}

	public void setQq(String qq) {
		this.qq = qq;
	}

	public String getQqNick() {
		return qqNick;
	}

	public void setQqNick(String qqNick) {
		this.qqNick = qqNick;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getTab() {
		return tab;
	}

	public void setTab(String tab) {
		this.tab = tab;
	}

	public String getMd5() {
		return md5;
	}

	public void setMd5(String md5) {
		this.md5 = md5;
	}

	@Override
	public String toString(){
		return "WeChatContacts [wxId=" + wxId 
				+ ", friendAcc=" + friendAcc
				+ ", friendNick=" + friendNick
				+ ", sex=" + sex
				+ ", signature=" + signature
				+ ", remarks=" + remarks
				+ ", location=" + location
				+ ", avatar=" + avatar
				+ ", qq=" + qq
				+ ", qqNick=" + qqNick
				+ ", mail=" + mail
				+ ", telephone=" + telephone
				+ ", description=" + description
				+ ", tab=" + tab
				+ ", md5=" + md5 + "]";
	}
}
