package com.xl.cloud.phoneBean;

//系统日志 应用程序使用记录 页面对象
public class AppUseRecord {
	//包名
	private String packageName;
	
	//最后更新时间
	private String lastUpdateTime;
	
	//包路径
	private String packagePath;
	
	//最新修改时间
	private String newestModifiedTime;
	
	//最后运行时间
	private String lastRunTime;
	
	//第一次安装时间
	private String firstInstallTime;
	
	//状态
	private String status;
	
	//md5加密
	private String md5;

	public String getPackageName() {
		return packageName;
	}

	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}

	public String getLastUpdateTime() {
		return lastUpdateTime;
	}

	public void setLastUpdateTime(String lastUpdateTime) {
		this.lastUpdateTime = lastUpdateTime;
	}

	public String getPackagePath() {
		return packagePath;
	}

	public void setPackagePath(String packagePath) {
		this.packagePath = packagePath;
	}

	public String getNewestModifiedTime() {
		return newestModifiedTime;
	}

	public void setNewestModifiedTime(String newestModifiedTime) {
		this.newestModifiedTime = newestModifiedTime;
	}

	public String getLastRunTime() {
		return lastRunTime;
	}

	public void setLastRunTime(String lastRunTime) {
		this.lastRunTime = lastRunTime;
	}

	public String getFirstInstallTime() {
		return firstInstallTime;
	}

	public void setFirstInstallTime(String firstInstallTime) {
		this.firstInstallTime = firstInstallTime;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getMd5() {
		return md5;
	}

	public void setMd5(String md5) {
		this.md5 = md5;
	}
	
	@Override
	public String toString(){
		return "AppUseRecord [packageName=" + packageName 
				+ ", lastUpdateTime=" + lastUpdateTime
				+ ", packagePath=" + packagePath
				+ ", newestModifiedTime=" + newestModifiedTime
				+ ", lastRunTime=" + lastRunTime
				+ ", firstInstallTime=" + firstInstallTime
				+ ", status=" + status
				+ ", md5=" + md5 + "]";
	}
}
