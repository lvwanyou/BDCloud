package com.xl.cloud.phoneBean;

//滴滴出行 个人设置对象
public class DiPersonSet {
	//家庭地址
	private String homeAdd;
	
	//公司地址
	private String compAdd;
	
	//家庭经度
	private String homeLongitude;
	
	//家庭纬度
	private String homeLatitude;
	
	//公司经度
	private String compLongitude;
	
	//公司维度
	private String compLatitude;
	
	//md5加密
	private String md5;

	public String getHomeAdd() {
		return homeAdd;
	}

	public void setHomeAdd(String homeAdd) {
		this.homeAdd = homeAdd;
	}

	public String getCompAdd() {
		return compAdd;
	}

	public void setCompAdd(String compAdd) {
		this.compAdd = compAdd;
	}

	public String getHomeLongitude() {
		return homeLongitude;
	}

	public void setHomeLongitude(String homeLongitude) {
		this.homeLongitude = homeLongitude;
	}

	public String getHomeLatitude() {
		return homeLatitude;
	}

	public void setHomeLatitude(String homeLatitude) {
		this.homeLatitude = homeLatitude;
	}

	public String getCompLongitude() {
		return compLongitude;
	}

	public void setCompLongitude(String compLongitude) {
		this.compLongitude = compLongitude;
	}

	public String getCompLatitude() {
		return compLatitude;
	}

	public void setCompLatitude(String compLatitude) {
		this.compLatitude = compLatitude;
	}

	public String getMd5() {
		return md5;
	}

	public void setMd5(String md5) {
		this.md5 = md5;
	}
	
	@Override
	public String toString(){
		return "DiPersonSet [homeAdd=" + homeAdd 
				+ ", compAdd=" + compAdd
				+ ", homeLongitude=" + homeLongitude
				+ ", homeLatitude=" + homeLatitude
				+ ", compLongitude=" + compLongitude
				+ ", compLatitude=" + compLatitude
				+ ", md5=" + md5 + "]";
	}
	
}